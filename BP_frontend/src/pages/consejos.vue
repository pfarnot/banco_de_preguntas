        <p>
          PASOS PARA ELABORAR UN CUESTIONARIO<br> En el proceso de elaboración de un
          cuestionario se deben contemplar los siguientes puntos: Definición del
          constructo o aspecto a medir Antes de proceder a medir algo debemos
          tener una idea muy clara de lo que queremos medir; a eso se le llama
          «definir el constructo». Ello puede requerir la realización de una
          revisión de la bibliografía y la consulta con expertos en la materia.
          Sean actitudes, conductas o conocimientos, se debe definir en forma
          clara y precisa el objeto de la medida y, a ser posible, determinar y
          conocer la teorías que sustentan la definición que se acuerde. Un
          problema puede definirse desde distintas perspectivas teóricas y, por
          tanto, pueden proponerse definiciones diferentes de un mismo
          constructo. Pensemos, por ejemplo, en la definición de la salud y su
          evolución; no será lo mismo diseñar un cuestionario de evaluación del
          estado de salud que defina la salud como la ausencia de enfermedad,
          que elaborar el cuestionario desde una perspectiva más positiva, de
          bienestar biopsicosocial. Finalmente, la validez del constructo
          implicará el grado en que el instrumento de medida refleja las teorías
          relevantes del fenómeno que mide. Propósito de la escala Se trata de
          establecer el contenido del cuestionario, definir la población a la
          que va dirigida, la forma de administración y el formato del
          cuestionario. El propósito de la escala va a determinar en gran medida
          el contenido de sus ítems y algunos aspectos relacionados con su
          estructura y la logística de la recogida de los datos. Si nuestro
          constructo o aspecto a medir fuera la calidad de vida, deberíamos
          revisar exhaustivamente todas las posibles características que
          conforman la calidad de vida: independencia física, vitalidad,
          equilibrio emocional, sueño, capacidad para relacionarse con
          otros...Cada una de estas características se denominan «dimensiones» o
          «factores», y la clara definición de cada una de ellas nos facilitará
          la construcción de las preguntas que nos ayuden a explorar esa parte
          del aspecto que queremos medir. Cuando se inicia el proceso de
          construcción de un cuestionario, se debe tener en cuenta la población
          a la que va dirigido, o, en el caso de que se elija un cuestionario ya
          validado, conocer con qué población (edad, patología, nivel cultural,
          intereses...) se ha validado el cuestionario. Por otro lado, de
          acuerdo con el tema que se vaya a estudiar, deberemos decidir cómo se
          va a administrar el cuestionario, si será autocumplimentado, si el
          procedimiento de recogida de la información será telefónico o si se
          realizará mediante una entrevista personal. Estos aspectos es
          importante tenerlos en cuenta, pues también obligarán a redactar las
          preguntas de forma distinta o incluso a dar un formato diferente al
          cuestionario. Cada una de las formas de recogida de información tiene
          sus ventajas e inconvenientes. Las encuestas telefónicas o mediante
          entrevista personal suelen tener mejor tasa de respuesta que las que
          se envían por correo postal, pero su coste también es más elevado y,
          en el caso de la encuesta telefónica, si el cuestionario es largo
          puede cansar al entrevistado. En ambos se requiere entrenar al
          encuestador o adjuntar un mínimo de instrucciones en los
          autoadministrados. Composición de los ítems Los cuestionarios se
          componen de una serie de ítems. El ítem es la unidad básica de
          información de un instrumento de evaluación, y generalmente consta de
          una pregunta y de una respuesta cerrada. Número de ítems Como regla
          general, se considera que el número mínimo de ítems para evaluar un
          fenómeno sería de 6, pero el número de ellos puede ir desde 10 a 90,
          de manera que puedan abarcar de forma proporcional cada una de las
          dimensiones definidas a priori en el constructo. Se recomienda
          realizar el doble de ítems de los que van a necesitarse en la versión
          definitiva del cuestionario. Contenido En función del contenido, los
          cuestionarios pueden ser uni o multidimensionales. En los primeros,
          más del 80% de los ítems evalúa una sola dimensión (por ejemplo, el
          cuestionario de dolor de McGill)5. En los multidimensionales, los
          ítems evalúan dos o más dimensiones (por ejemplo, las escalas de
          calidad de vida, SF-361). Definición y ordenación La definición de
          cada ítem ha de ser exhaustiva y mutuamente excluyente. Por otro lado,
          al formular la pregunta deben tenerse en cuenta factores como la
          comprensión (es necesario adaptar el lenguaje y el tipo de elección de
          respuestas al nivel sociocultural de los individuos a quienes va
          dirigido el cuestionario), así como la aceptabilidad para el sujeto
          que es preguntado. Existen una serie de criterios para la redacción de
          las preguntas, que son los siguientes: • Utilizar preguntas breves y
          fáciles de comprender. • No emplear palabras que induzcan una reacción
          estereotipada. • No redactar preguntas en forma negativa. • Evitar el
          uso de la interrogación «por qué». • No formular preguntas en las que
          una de las alternativas de respuesta sea tan deseable que difícilmente
          pueda rehusarse. • Evitar preguntas que obliguen a hacer cálculos o
          esfuerzos de memoria. En la figura 1 se muestran ejemplos de criterios
          a considerar en relación con la formulación de las preguntas. Una vez
          redactados los ítems, éstos deben ordenarse. A veces, la lógica del
          cuestionario nos orientará sobre el orden seguir, pero si no fuera así
          se pueden ordenar aleatoriamente. Prevención de los sesgos en su
          cumplimentación También a través del diseño de los ítems del
          cuestionario se pueden controlar los posibles sesgos de
          cumplimentación y, con ello, mejorar la validez del
          cuestionario.Aunque se ha descrito un número importante de sesgos,
          aquí citaremos sólo algunos de los más frecuentes. Un sesgo habitual
          es el «error de tendencia central», es decir, la tendencia a elegir
          entre las diferentes opciones de respuesta, las centrales. Otro sesgo
          también frecuente es el de «deseabilidad social», que se produce
          cuando se responde en función de lo que se considera socialmente
          aceptable. El «sesgo de aprendizaje o de proximidad» induce a
          contestar de forma similar a las respuestas anteriores. El «error
          lógico» se produce cuando el encuestado considera que todos los ítems
          relacionados deben puntuarse igual. Finalmente, hay que tener siempre
          en cuenta la redacción del cuestionario (evitando las preguntas de
          comprensión dudosa, preguntas dobles…), el orden de las preguntas o de
          las categorías de respuesta (irradiación de respuesta) y el formato,
          en cuanto que puede impedir el registro adecuado de la información.
          Todo ello puede introducir sesgos en la recogida de la información.
          Codificación de las respuestas En función del número de opciones o
          tipo de respuestas, éstas pueden ser: – Dicotómicas:
          Sí/No,Verdadero/Falso. – Policotómicas: Estoy descontento conmigo
          mismo/ No me valoro/ Me odio/ Estoy satisfecho de mí mismo. –
          Analógicas: Se diferencian en función del sistema analógico utilizado
          (figura 2). Puntuación de los ítems Lógicamente, también es necesario
          definir el sistema de puntuación que va a emplearse: simple o
          ponderado. Se dice que son ítems simples cuando la puntuación directa
          se obtiene con el sumatorio de respuestas acertadas o de los valores
          que se hayan dado a cada opción. Se habla de ítems ponderados cuando
          el valor de cada opción de respuesta no es la misma o no se otorga el
          mismo valor a todos los aciertos. Proceso de validación Una vez
          diseñado el borrador definitivo, es decir, una vez delimitada la
          información, formuladas las preguntas, definido el número de ellas que
          vamos a incluir en el cuestionario y ordenadas las preguntas,
          corresponde llevar a cabo la realización de la prueba piloto y la
          evaluación de las propiedades métricas da la escala. Prueba piloto o
          pretest cognitivo Normalmente, se pasa el borrador del cuestionario a
          3050 personas, siendo aconsejable que se parezcan a los individuos de
          la muestra. Este pretest permitirá identificar: • Tipos de preguntas
          más adecuados. • Si el enunciado es correcto y comprensible, y si las
          preguntas tienen la extensión adecuada. • Si es correcta la
          categorización de las respuestas. • Si existen resistencias
          psicológicas o rechazo hacia algunas preguntas. • Si el ordenamiento
          interno es lógico; si la duración está dentro de lo aceptable por los
          encuestados. En cuanto a los métodos utilizados para la realización
          del pretest cognitivo, éste se lleva a cabo mediante la realización
          de: • Entrevistas informales. • Grupos focales de la población diana.
          • Encuesta sobre comprensión de las preguntas. • Valoración del
          cuestionario por parte de los participantes en el estudio. Evaluación
          de las propiedades métricas de la escala Dado que lo que se está
          diseñando es una escala de medición que permita tener una puntuación
          de un aspecto de la salud, y poder comparar la de diferentes
          individuos o la del mismo individuo en diferentes momentos, se debe
          asegurar que el instrumento de medida sea fiable y válido. Fiabilidad
          Es el grado en que un instrumento mide con precisión, sin error.
          Indica la condición del instrumento de ser fiable, es decir, de ser
          capaz de ofrecer en su empleo repetido resultados veraces y constantes
          en condiciones similares de medición. La fiabilidad de un instrumento
          de medida se valora a través de la consistencia, la estabilidad
          temporal y la concordancia interobservadores. • Consistencia: Se
          refiere al nivel en que los diferentes ítems o preguntas de una escala
          están relacionados entre sí. Esta homogeneidad entre los ítems nos
          indica el grado de acuerdo entre los mismos y, por tanto, lo que
          determinará que éstos se puedan acumular y dar una puntuación global.
          La consistencia se puede comprobar a través de diferentes métodos
          estadísticos. El coeficiente alfa de Cronbach es un método estadístico
          muy utilizado. Sus valores oscilan entre 0 y 1. Se considera que
          existe una buena consistencia interna cuando el valor de alfa es
          superior a 0,7. • Estabilidad temporal: Es la concordancia obtenida
          entre los resultados del test al ser evaluada la misma muestra por el
          mismo evaluador en dos situaciones distintas (fiabilidad test-retest).
          La fiabilidad (normalmente calculada con el coeficiente de correlación
          intraclase [CCI], para variables continuas y evaluaciones temporales
          distantes) nos indica que el resultado de la medida tiene estabilidad
          temporal. Una correlación del 70% indicaría una fiabilidad aceptable.
          • Concordancia interobservadores. En el análisis del nivel de acuerdo
          obtenido al ser evaluada la misma muestra en las mismas condiciones
          por dos evaluadores distintos, o en diferente tiempo, se obtienen
          iguales resultados-fiabilidad interobservadores). La concordancia
          entre observadores se puede analizar mediante el porcentaje de acuerdo
          y el índice Kappa. Validez Es el grado en que un instrumento de medida
          mide aquello que realmente pretende medir o sirve para el propósito
          para el que ha sido construido. A pesar de que se describen diferentes
          tipos de validez, ésta, sin embargo, es un proceso unitario y es
          precisamente la validez la que permitirá realizar las inferencias e
          interpretaciones correctas de las puntuaciones que se obtengan al
          aplicar un test y establecer la relación con el constructo/variable
          que se trata de medir. • Validez de contenido. Se refiere a si el
          cuestionario elaborado, y por tanto los ítems elegidos, son
          indicadores de lo que se pretende medir. Se trata de someter el
          cuestionario a la valoración de investigadores y expertos, que deben
          juzgar la capacidad de éste para evaluar todas las dimensiones que
          deseamos medir. No cabe, por tanto, cálculo alguno sólo las
          valoraciones cualitativas que los investigadoresexpertos deben
          efectuar. • Validez de constructo. Evalúa el grado en que el
          instrumento refleja la teoría del fenómeno o del concepto que mide. La
          validez de construcción garantiza que las medidas que resultan de las
          respuestas del cuestionario pueden ser consideradas y utilizadas como
          medición del fenómeno que queremos medir. Puede ser calculada por
          diversos métodos, pero los más frecuentes son el análisis factorial y
          la matriz multirrasgo-multimétodo. • Validez de criterio. Relación de
          la puntuación de cada sujeto con un Gold Standard que tenga garantías
          de medir lo que deseamos medir. No siempre hay disponibles indicadores
          de referencia, por lo que, muchas veces, en la práctica se recurre a
          utilizar instrumentos que han sido respaldados por otros estudios o
          investigaciones y nos ofrecen garantías de medir lo que deseamos
          medir. Dependiendo del tipo de variables, utilizaremos coeficientes de
          correlación de Pearson (variables cuantitativas) o cálculo de la
          sensibilidad y especificidad (variables cualitativas). En resumen, y
          para clarificar lo que se pretende con la validez y la fiabilidad
          podemos destacar los siguientes puntos: 1) lo que se valida no es el
          test, sino las puntuaciones del test, y por tanto, la pregunta que
          tratamos de responder es: ¿es válido el uso de las puntuaciones de
          este test?; 2) la validez no se puede resumir en un solo indicador o
          índice numérico, al igual que ocurría con la fiabilidad (coeficiente
          de fiabilidad, alfa de Cronbach, etc.); 3) la validación es un proceso
          continuo y dinámico, y 4) la teoría desempeña un papel muy importante
          como guía tanto del desarrollo de un test como de su proceso de
          validación. VALIDEZ Y ADAPTACIÓN DE ESCALAS La adaptación o traducción
          de escalas es una práctica bastante habitual de los investigadores en
          ciencias de la salud. El proceso de traducción y adaptación de una
          escala requiere algo más que la traducción de la lengua origen a la
          lengua de destino: es necesario asegurar que las puntuaciones
          obtenidas con el test traducido son equivalentes a las obtenidas con
          el test original (tabla 1). Para alcanzar esa equivalencia, hay que
          considerar cuatro aspectos del proceso: 1) el contexto cultural donde
          se va a realizar la adaptación, 2) aspectos técnicos del propio
          desarrollo y adaptación del test, 3) administración del test, y 4)
          interpretación de las puntuaciones. En definitiva, es necesario
          asegurar que el instrumento de medida presenta las mismas propiedades
          métricas en las dos culturas (origen y destino), y que, por lo tanto,
          la interpretación de las puntuaciones es la misma, es decir, que
          existe una equivalencia métrica.
